package if_else;

public class switchOne {
    public static void main(String args[]){
        int number=2;
        switch(number){
            case 1:System.out.println("1");break;
            case 2:System.out.println("2");break;
            case 3:System.out.println("3");break;
            default: System.out.println("nananna");
        }
    }
}
